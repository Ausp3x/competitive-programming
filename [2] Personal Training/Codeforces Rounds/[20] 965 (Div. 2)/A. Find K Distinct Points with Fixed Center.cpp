#include <ext/pb_ds/assoc_container.hpp>
using namespace std;
using namespace __gnu_pbds;

#define fi first
#define se second
#define pb push_back
// #define DEBUG
typedef long long    lng;

int const INF32 = 0x3f3f3f3f;
lng const INF64 = 0x3f3f3f3f3f3f3f3f;

void solve() {
    int x, y, k;
    cin >> x >> y >> k;

    if (k % 2 == 1)
        cout << x << ' ' << y << endl;

    k /= 2;
    for (int i = 1; i <= k; i++) {
        cout << x - i << ' ' << y << endl;
        cout << x + i << ' ' << y << endl;
    }

    return;
}

int main() {
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);

    int t = 1;
    cin >> t;
    while (t--) {
        solve();
    }

    return 0;
}