void solve() {
    int l = 0, r = 1000;
    while (r - l > 1) {
        int md = (l + r) / 2;

        cout << "? " << md << ' ' << md << endl;
        int res;
        cin >> res;

        if (res == md * md)
            l = md;
        else
            r = md;
    }

    cout << "! " << r << endl;
    
    return;
}

